    <h3>André Drop | Arrangementen</h3>
    <br />
    <br />
    <p> Zoals hij het zichzelf herinnert is hij al &quot;sinds mensenheugenis&quot;        gepassioneerd bezig met jazzmuziek. Aanvankelijk - vanaf de vroege
        tienerjaren - op piano, geïnspireerd door Nat &quot;King&quot; Cole en George Shearing
        en wat later ook op trompet en verwante blaasinstrumenten.</p>
        
    <p> Na zijn
        verhuizing van Dordrecht naar Den Haag halverwege de jaren vijftig -
        toentertijd bepaald het Mekka van de Nederlandse  moderne jazz (zonder
        Eindhoven en Haarlem tekort te doen!) - had hij het geluk te worden
        opgenomen in een muzikale vriendenkring rond Frans Elsen, Rob Madna en Rob
        Agerbeek, een zeer stimulerende omgeving, niet in het minst wat betreft zijn
        altijd al aanwezige fascinatie met meerstemmigheid, die de aanleiding vormde
        om ook te gaan arrangeren, vooral voor middelgrote ensembles - de Birth of
        the Cool Band van Gil Evans en Miles Davis was tenslotte een van de
        favorieten bij zijn middelbare schoolvriendjes. </p>
        
    <p>En nu dan voor Jazzcination!</p>
