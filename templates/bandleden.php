    <!-- FRED RIJSDIJK-->
    <h2><img width="200" height="200" alt="Fred Rijsdijk Gitaar" src="../afbeeldingen/gitarist200x200.jpg" /></h2>
    <h2>Gitaar: Fred Rijsdijk</h2>
    <p>
            Fred Rijsdijk begon op 11 jarige leeftijd gitaar te spelen en heeft
            in
            de 60-/70-er jaren in diverse bands gespeeld. Na een lange periode
            de
            gitaar niet meer te hebben aangeraakt is hij klassieke gitaar gaan
            studeren aan de Muziekschool te  Valkenswaard waar hij in 1999
            zijn eind (D) diploma haalde. </p>
    <p>Sinds 2001 is Fred zich
            gaan richten op de jazzmuziek via zelfstudie en lessen van Herbie
            Guldenaar en Paul Hagenaars.Hij speelt in de bigbands "Nightshift"
            te
            Valkenswaard en "Lighttown" te Eindhoven, in diverse gelegenheid-
            jazzcombo's en in de jazzformatie "Jazzcination" die hij met pianist
            Jan Lohstroh oprichtte in 2011.</p>
