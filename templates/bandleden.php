    <!-- MEINDERT JANSZEN-->
    <h2><img width="200" height="200" alt="meinder janszen trombone" src="../afbeeldingen/trombone200x200.jpg" /></h2>
    <h2>Trombone: Meindert Janszen</h2>
    <p>Meindert
            Janszen is op 6 jarige leeftijd begonnen met trombone spelen (dat
            was
            het enige instrument waar hij direct geluid uit kreeg) op de
            muziekschool in Emmeloord. Daar heeft hij tot zijn 15e jaar les
            gehad
            van de jazztrompettist, Wim de Soet, die naast de klassieke
            scholing  ook met de jazzmuziek en improviseren aan de gang ging,
            en daar zodoende de basis en interesse voor lichte muziek legde. Hij
            speelde daar ook voor het eerst in een Bigband.</p>
    <p>Na de
            studietijd, waarin zeer weinig werd gespeeld, ging hij in Helmond
            wonen
            en is hij als leadtrombonist gaan spelen bij "Bigband Geldrop" en in
            een voor die tijd vooruitstrevend carnavalsorkest in Helmond, de
            "DAKkapel", waarmee de titel: "Meest originele straatorkest van
            Nederland  1995" in Bemmel werd gewonnen. Gedurende enkele jaren
            deed hij mee aan diverse jazzworkshops in Eindhoven en Helmond en
            heeft
            hij, als lid van het begeleidingsorkest, de theatertour van Petazzi
            (een bekende Helmondse amusementsgroep) meegemaakt. Sinds enkele
            jaren
            speelt hij als leadtrombonist in de Eindhovense "Lighttown Bigband"
            en
            sinds kort in de jazzformatie "Jazzcination".</p>
