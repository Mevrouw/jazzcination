    <h2>Toetsen: Jan Lohstroh</h2>
    <p>Jan Lohstroh begon op 10 jarige leeftijd piano te spelen en kreeg les
            van
            Christiaan Kuijten in Tilburg. Tijdens zijn middelbare schooltijd
            deed
            hij mee in diverse schoolbandjes. Gedurende zijn studiejaren daarna
            bleef de piano onaangeroerd, maar luisterde hij veel naar
            jazz-muziek.
            Daarna pakte hij het klassieke pianospel op en nam hij lessen bij
            achtereenvolgens Willy Paulik en Corn√©lie Hoendervanger in
            Eindhoven.
            Momenteel neemt hij klassieke lessen bij Jacques Wijnen in
            Rousillon-en-Morvan. </p>
    <p>Sinds 2006 heeft hij de jazz
            (her)ondekt en was pianist bij "Big Band Geldrop" van 2006 tot mid
            2012. Hij is sinds 2007 pianist bij de "Lighttown Bigband" in
            Eindhoven. Hij arrangeert ook stukken voor Big Band en componeert
            jazzthema's. Hij nam lessen in arrangeren en improviseren bij
            Richard
            Beeren. Sinds 2008 speelt hij in diverse gelegenheids-combo's en in
            de
            jazzformatie "Jazzcination" die hij met gitarist Fred Rijsdijk
            oprichtte in 2011.</p>
