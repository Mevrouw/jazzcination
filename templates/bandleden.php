    <h2><img width="200" height="200" alt="trompet harm langermans" src="../afbeeldingen/Monique%20dorst.jpg" /></h2>
    <h2>Vocals: Monique Dorst </h2>
    <p>Monique Dorst is op 11 jarige leeftijd begonnen met muziek te
            maken. Eerst op klarinet bij een Harmonie in Noordwijk, later ook op
            alt saxofoon bij een fanfare in Noordwijkerhout. Vanaf 1988 speelde
            ze bij Big Band Geldrop op alt saxofoon en schakelde daar vanaf 2000
            geleidelijk over naar zang en werd ze uiteindelijk de zangeres van
            deze big band; ze zong bij Big Band Geldrop tot 2007.
            Grote inspirator om te gaan zingen was de mooie warme stem van Karen
            Carpenter. </p>
    <p>Monique volgde een zangopleiding bij de Academie Voor Lichte Muziek
            Muziek (AVLM) in Lelystad en nam zanglessen bij Hella Hertogs. Ze
            zingt als achtergrond- en lead zangeres in "The Fabulous Party Band"
            en sinds 2014, na een aantal gastoptredens, als vaste zangeres bij
            Jazzcination. </p>
